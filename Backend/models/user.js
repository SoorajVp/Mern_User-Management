const mongoose = require('mongoose');

const userSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true
  },
  email: {
    type: String,
    required: true,
    unique: true
  },
  mobile: {
    type: String,
    required: true,
    unique: true
  },
  password: {
    type: String,
    required: true
  },
  profilePic: {
    type: String,
    default:
      "https://cdn-icons-png.flaticon.com/512/149/149071.png",
  },
});

const User = mongoose.model('Users', userSchema);

module.exports = User;